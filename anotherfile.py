def compose(num1,num2,op):
    if op == "+":
        print(num1+num2)
    
    elif op == "-":
        print(num1-num2)
    elif op == "*":
        print(num1*num2)
    elif op == "/":
        print(num1/num2)
    elif op == "%":
        print(num1%num2)

    else :
        print("invalid operator input!")
    